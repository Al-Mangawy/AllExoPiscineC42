char	*ft_strcat(char *dest, char *src)
{
	int r;
	int c;

	r = 0;
	c = 0;
	while (dest[r] != '\0')
	{
		r++;
	}
	while (src[c] != '\0')
	{
		dest[r + c] = src[c];
		c++;
	}
	dest[r + c] = '\0';
	return (dest);
}
