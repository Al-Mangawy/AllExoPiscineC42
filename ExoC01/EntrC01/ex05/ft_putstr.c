#include <unistd.h>

void	ft_putchar(char r)
{
	write(1, &r, 1);
}

void	ft_putstr(char *str)
{
	int n;

	n = 0;
	while (str[n] != '\0')
	{
		ft_putchar(str[n]);
		n++;
	}
}
