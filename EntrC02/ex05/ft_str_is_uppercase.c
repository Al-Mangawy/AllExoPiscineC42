#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int n;

	n = 0;
	while (str[n] != '\0')
	{
		if (!(str[n] >= 65 && str[n] <= 90))
		{
			return (0);
		}
		n++;
	}
	return (1);
}
