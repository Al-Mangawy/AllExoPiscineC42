#include <unistd.h>

void	ft_putnbr(int nb)
{
	char		r;
	long int	n;

	n = nb;
	if (n < 0)
	{
		r = '-';
		write(1, &r, 1);
		n = -n;
	}
	if (n < 10)
	{
		r = n + 48;
		write(1, &r, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		n = n - (n / 10) * 10;
		r = n + 48;
		write(1, &r, 1);
	}
}
